//метод, который выводит палец вверх на консоль
void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            // Console.Write($"{image[i, j]} ");
            if(image[i, j] == 0) Console.Write($" ");  //заменили нули и единицы на пробелы и плюсики
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

//метод, который закрашивает картинку

void FillImage(int row, int col)
{
    if(pic[row, col] == 0)
    {
        pic[row,col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}

